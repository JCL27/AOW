package ar.edu.itba.game;

public class MeleeUnit extends Unit {
	static int HP;
	static int MAX_HP;
	static double ATTACK_SPEED;
	static int ATTACK_RANGE;
	static int MOVEMENT_SPEED;
	static int DAMAGE;
	static Element ELEMENT;
	
	public MeleeUnit(){
		this.maxHp = MAX_HP;
		this.hp = MAX_HP;
		this.attackSpeed = ATTACK_SPEED;
		this.attackRange = ATTACK_RANGE;
		this.movementSpeed = MOVEMENT_SPEED;
		this.damage = DAMAGE;
		this.element = new Element(Textures.BLUE_BALL, 1000, 0, 3, 0, 6, false);
		this.dir = 	Side.LEFT;
	}
}
